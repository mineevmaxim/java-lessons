public class Strings {
    public static void main(String[] args) {
        String myString = "Hello";
        String space = " ";
        String name = "World";
        System.out.println(myString + space + name);
        System.out.println("Hello" + " " + "Worlds");
        System.out.println("2 + 2 = " + (2 + 2));
    }
}
